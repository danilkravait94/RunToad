        static void Paint()
        {
            for (int i = 0; i < 27; i++)
            {
                for (int f = 0; f < 31; f++)
                {
                    Console.SetCursorPosition(f, i);
                    if (map[i, f] == 1) { Console.Write("Ö"); }
                    else if (map[i, f] == 2) { Console.Write(">"); }
                    else if (map[i, f] == 3) { Console.Write("<"); }
                    else if (map[i, f] != -1) { Console.Write(" "); }

                }
            }
        }
        static void MoveOfToad()
        {
            Console.SetCursorPosition(0, 0);
            if (Console.KeyAvailable)
            {
                k = Console.ReadKey();
                pressed = true;
            }
            else
                pressed = false;

            if (k.Key == ConsoleKey.UpArrow && pressed) { x -= 2; }
            else if (k.Key == ConsoleKey.LeftArrow && pressed && Left()) { y -= 2; }
            else if (k.Key == ConsoleKey.RightArrow && pressed && Right()) { y += 2; }
            else if (k.Key == ConsoleKey.DownArrow && pressed && Down()) { x += 2; }
            else if (k.Key == ConsoleKey.D1 && pressed)
            {
                Console.Write(" ");
                Console.SetCursorPosition(36, 1);
                Console.WriteLine("Level 1");
                speed = 40;
            }
            else if (k.Key == ConsoleKey.D2 && pressed)
            {
                Console.SetCursorPosition(36, 1);
                Console.WriteLine("Level 2");
                speed = 20;
            }
            else if (k.Key == ConsoleKey.D3 && pressed)
            {
                Console.SetCursorPosition(36, 1);
                Console.WriteLine("Level 3");
                speed = 10;
            }
            Console.SetCursorPosition(0, 0);
            Console.Write(cube);
            Restart();
        }
